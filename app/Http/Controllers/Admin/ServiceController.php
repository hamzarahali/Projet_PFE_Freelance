    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\service  $service
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, service $service)
    {
        $request->validate([
            'nom' => 'required',
            'image' => 'required',
            'description' => 'required',
            'benefice' => 'required',
        ]);

        $filename = 'services/'.Str::uuid().'.png';
        $path = public_path($filename);
        $image = Image::make(file_get_contents($request->image))->save($path);
     

        if ($request->has('image2')) { 
            $filename2 = 'services/'.Str::uuid().'.png';
            $path2 = public_path($filename2);
            $image = Image::make(file_get_contents($request->image2))->save($path2);
        }


        $service->update([
            'nom'           => $request->nom,
            'image'         => $filename,
            'image2'        => $filename2 ?? $service->image2,
            'description'   => $request->description,
            'benefice'      => $request->benefice
        ]);
  
        return redirect()->route('admin.services.index')
                        ->with('success','Service modifié avec succès');
    }

